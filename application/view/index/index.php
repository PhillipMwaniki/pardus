<h1>Welcome SSG Admin Portal</h1>
<p>SSG is a SMS gateway that abstracts all SDP operations. It handles all communication to and from Safaricom Service Development Platform (SDP) for all SMS related functions.</p>

<p>The SSG Admin portal allows the admin user to carry out the following operations:</p>

<ul>
	<li><strong>Service management functions </strong>– add, edit, delete, view, enable and disable services on the system</li>
    <li><strong>Inbox management functions</strong> – query inbound messages, generate inbound messages report</li>
    <li><strong>Inbox management functions</strong> – query inbound messages, generate inbound messages report</li>
    <li><strong>Outbox management functions</strong> – query outbound messages, and generate outbound report</li>
    <li><strong>Delivery receipts management functions</strong> – query delivery receipt messages, and generate delivery receipt report</li>
    <li><strong>Subscription management functions</strong> – query subscriptions requests, and generate subscription requests report</li>
</ul>


